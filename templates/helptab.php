    <h2>Simple Cookies</h2>

    <ul>
        <li><code>[addsimplecookie parameter="" value="" time=""]</code> — adds cookie.</li>
        <li><code>[showforsimplecookie parameter="" value=""]</code> <span class="color-grey">Content</span> <code>[/showforsimplecookie]</code>
            — shows content if user has the cookie.
        </li>
        <li><code>[hideforsimplecookie parameter="" value=""]</code> <span class="color-grey">Content</span> <code>[/hideforsimplecookie]</code>
            — hides content if user has the cookie.
        </li>
        <li><code>[removesimplecookie parameter="" value=""]</code> — removes cookie.</li>
    </ul>
